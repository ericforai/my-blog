// Helper function to get translation
export function getTranslation(locale: string, key: string): string {
  return ui[locale as keyof typeof ui]?.[key as keyof typeof ui['en']] || key;
}

// Helper function to get current locale from URL
export function getLocaleFromPath(pathname: string): string {
  if (pathname.startsWith('/zh/')) return 'zh';
  return 'en';
}

// Helper function to get alternate language path
export function getAlternatePath(pathname: string, targetLocale: string): string {
  const currentLocale = getLocaleFromPath(pathname);
  
  if (currentLocale === targetLocale) return pathname;
  
  if (targetLocale === 'en') {
    // Remove /zh prefix
    return pathname.replace(/^\/zh/, '') || '/';
  } else {
    // Add /zh prefix
    return pathname === '/' ? '/zh/' : `/zh${pathname}`;
  }
}
